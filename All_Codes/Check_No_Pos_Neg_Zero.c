#include<stdio.h>

int main()
{
    int n;
    printf("Enter a no: ");
    scanf("%d",&n);
    if(n > 0)
    {
        printf("%d is Positive no. \n",n);
    }
    else if(n < 0)
    {
        printf("%d is Negative no. \n",n);
    }
    else
    {
        printf("%d is Negative no. \n",n);
    }
}
